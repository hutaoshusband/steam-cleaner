// src/main.rs
#![windows_subsystem = "windows"]

mod core;
mod ui;

use iced::{Application, Settings};

fn main() -> iced::Result {
    #[cfg(windows)]
    {
        if !core::privileges::is_elevated() {
            core::privileges::show_admin_error_dialog();
            std::process::exit(1);
        }
    }

    // Configure window for iOS-style transparency
    let window_settings = iced::window::Settings {
        decorations: false,  // Remove Windows title bar
        transparent: true,    // Enable transparency
        size: iced::Size::new(1200.0, 800.0), // Larger start size
        resizable: true,      // Allow resizing
        ..Default::default()
    };

    ui::app::CleanerApp::run(Settings {
        window: window_settings,
        ..Default::default()
    })
}
